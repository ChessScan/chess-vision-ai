def main():
    """Generate all chess pieces."""
    
    print("=== Simple Chess Piece Generator ===")
    print(f"Output directory: {OUTPUT_DIR}")
    
    # Create output directories
    white_dir = OUTPUT_DIR / "white"
    black_dir = OUTPUT_DIR / "black"
    white_dir.mkdir(parents=True, exist_ok=True)
    black_dir.mkdir(parents=True, exist_ok=True)
    
    pieces = ['king', 'queen', 'rook', 'bishop', 'knight', 'pawn']
    
    print("\nGenerating pieces...")
    
    for piece in pieces:
        print(f"  Creating {piece}...")
        
        if piece == 'king':
            func = generate_king
        elif piece == 'queen':
            func = generate_queen
        elif piece == 'rook':
            func = generate_rook
        elif piece == 'bishop':
            func = generate_bishop
        elif piece == 'knight':
            func = generate_knight
        else:  # pawn
            func = generate_pawn
        
        # Generate both colors (same geometry, materials differ)
        white_path = white_dir / f"{piece}.obj"
        black_path = black_dir / f"{piece}.obj"
        
        func(str(white_path))
        func(str(black_path))
        
        print(f"    ✓ {white_path}")
        print(f"    ✓ {black_path}")
    
    print("\n=== Complete ===")
    print(f"Generated {len(pieces) * 2} OBJ files in {OUTPUT_DIR}")
    print("\nNote: These are simplified geometric pieces.")
    print("For higher quality, download detailed scanned models.")
    print("These are sufficient for initial pipeline testing.")
    print("\nColor is handled by materials, not geometry.")

if __name__ == "__main__":
    main()