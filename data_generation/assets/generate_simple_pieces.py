#!/usr/bin/env python3
"""
Generate simple chess pieces as OBJ files (no Blender required)
OBJ is a text-based mesh format - we can generate it programmatically.

Usage: python3 generate_simple_pieces.py
"""

import os
import math
from pathlib import Path

OUTPUT_DIR = Path(__file__).parent / "pieces" / "generated_basic"

# Piece dimensions in generic units (to be scaled later)
# These are simplified geometric representations

def write_obj(filepath, vertices, faces, normals=None, uvs=None):
    """Write an OBJ file from mesh data."""
    with open(filepath, 'w') as f:
        f.write("# Simple procedural chess piece\n")
        f.write(f"# Generated by generate_simple_pieces.py\n\n")
        
        # Write vertices
        for v in vertices:
            f.write(f"v {v[0]:.6f} {v[2]:.6f} {v[1]:.6f}\n")  # OBJ uses Y-up, we use Z-up
        
        f.write(f"\n# {len(vertices)} vertices\n\n")
        
        # Write normals if provided
        if normals:
            for n in normals:
                f.write(f"vn {n[0]:.6f} {n[2]:.6f} {n[1]:.6f}\n")
        
        # Write UVs if provided
        if uvs:
            for uv in uvs:
                f.write(f"vt {uv[0]:.6f} {uv[1]:.6f}\n")
        
        # Write faces (1-indexed in OBJ)
        f.write(f"\n# {len(faces)} faces\n\n")
        for face in faces:
            if len(face) == 3:
                f.write(f"f {face[0]+1} {face[1]+1} {face[2]+1}\n")
            elif len(face) == 4:
                f.write(f"f {face[0]+1} {face[1]+1} {face[2]+1} {face[3]+1}\n")

def create_cylinder_vertices(radius, height, segments=16):
    """Create vertices for a cylinder."""
    vertices = []
    
    # Bottom circle
    for i in range(segments):
        angle = 2 * math.pi * i / segments
        x = radius * math.cos(angle)
        y = radius * math.sin(angle)
        vertices.append((x, y, 0))
    
    # Bottom center
    vertices.append((0, 0, 0))
    
    # Top circle
    for i in range(segments):
        angle = 2 * math.pi * i / segments
        x = radius * math.cos(angle)
        y = radius * math.sin(angle)
        vertices.append((x, y, height))
    
    # Top center
    vertices.append((0, 0, height))
    
    return vertices

def create_cylinder_faces(segments=16):
    """Create face indices for a cylinder."""
    faces = []
    bottom_center = segments
    top_center = 2 * segments + 1
    
    # Bottom faces
    for i in range(segments):
        next_i = (i + 1) % segments
        faces.append([i, next_i, bottom_center])
    
    # Top faces
    for i in range(segments):
        next_i = (i + 1) % segments
        faces.append([segments + i, top_center, segments + next_i])
    
    # Side faces (quads)
    for i in range(segments):
        next_i = (i + 1) % segments
        v0 = i
        v1 = next_i
        v2 = segments + next_i
        v3 = segments + i
        faces.append([v0, v1, v2, v3])
    
    return faces

def generate_king(output_path):
    """Generate a basic king piece."""
    vertices = []
    faces = []
    
    # Base
    base_r = 1.8
    base_h = 0.5
    
    # Body - wide cylinder
    body_r = 1.2
    body_h = 2.5
    
    # Neck
    neck_r = 0.8
    neck_h = 0.4
    
    # Head
    head_r = 1.0
    head_h = 1.0
    
    # We'll build this as stacked cylinders with different segment resolutions
    segments = 32
    
    # Simplified: single tapered cylinder for now
    v_base = create_cylinder_vertices(base_r, base_h, segments)
    f_base = create_cylinder_faces(segments)
    
    # Offset body to sit on base
    v_body = create_cylinder_vertices(body_r, body_h, segments)
    v_body = [(v[0], v[1], v[2] + base_h) for v in v_body]
    f_body = [[f + len(v_base) for f in face] for face in create_cylinder_faces(segments)]
    
    # Neck
    v_neck = create_cylinder_vertices(neck_r, neck_h, segments)
    v_neck = [(v[0], v[1], v[2] + base_h + body_h) for v in v_neck]
    f_neck = [[f + len(v_base) + len(v_body) for f in face] for face in create_cylinder_faces(segments)]
    
    # Head
    v_head = create_cylinder_vertices(head_r, head_h, segments)
    v_head = [(v[0], v[1], v[2] + base_h + body_h + neck_h) for v in v_head]
    f_head = [[f + len(v_base) + len(v_body) + len(v_neck) for f in face] for face in create_cylinder_faces(segments)]
    
    # Combine
    all_vertices = v_base + v_body + v_neck + v_head
    all_faces = f_base + f_body + f_neck + f_head
    
    write_obj(output_path, all_vertices, all_faces)

def generate_queen(output_path):
    """Generate a basic queen piece (similar to king but shorter)."""
    vertices = []
    
    # Simplified - use same cylinder approach with different proportions
    base_r = 1.8
    base_h = 0.5
    body_r = 1.2
    body_h = 2.0
    neck_r = 0.8
    neck_h = 0.3
    head_r = 1.1
    head_h = 0.8
    
    segments = 32
    
    v_base = create_cylinder_vertices(base_r, base_h, segments)
    v_body = create_cylinder_vertices(body_r, body_h, segments)
    v_body = [(v[0], v[1], v[2] + base_h) for v in v_body]
    v_neck = create_cylinder_vertices(neck_r, neck_h, segments)
    v_neck = [(v[0], v[1], v[2] + base_h + body_h) for v in v_neck]
    v_head = create_cylinder_vertices(head_r, head_h, segments)
    v_head = [(v[0], v[1], v[2] + base_h + body_h + neck_h) for v in v_head]
    
    # Add crown points (simplified as pyramid on top)
    crown_points = []
    crown_height = head_h + 0.6
    for i in range(4):
        angle = i * math.pi / 2 + math.pi / 4
        x = head_r * 0.6 * math.cos(angle)
        y = head_r * 0.6 * math.sin(angle)
        crown_points.append((x, y, crown_height))
    
    all_vertices = v_base + v_body + v_neck + v_head + crown_points
    
    # Basic faces (simplified - just the cylinders for now)
    f_base = create_cylinder_faces(segments)
    f_body = [[f + len(v_base) for f in face] for face in create_cylinder_faces(segments)]
    f_neck = [[f + len(v_base) + len(v_body) for f in face] for face in create_cylinder_faces(segments)]
    f_head = [[f + len(v_base) + len(v_body) + len(v_neck) for f in face] for face in create_cylinder_faces(segments)]
    
    all_faces = f_base + f_body + f_neck + f_head
    
    write_obj(output_path, all_vertices, all_faces)

